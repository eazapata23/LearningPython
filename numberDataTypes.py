# complex numbers are an extension of the familiar real number system in which all numbers
# are expressed as a sum of a real part and an imaginary part
# imaginary numbers are real multiples of the imaginary unit which is the square root of negative one
# often written i in mathematics or j in engineering
num1 = 2+3j
num2 = complex(2, 3)

print(num2.real, num2.imag)

# abs returns the absolute value of a number
print(abs(-5.5))

# round is going to round to the nearest integer
print(round(5.5))
